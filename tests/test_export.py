"""Tests for the export commands."""

from __future__ import annotations

import csv
import io
from pathlib import Path

import pytest
from click.testing import CliRunner

from nexus.cli import cli
from nexus.commands.export import _build_markdown, _build_tasks_csv, _build_timelog_csv
from nexus.db import Database
from nexus.models import Priority, Status


@pytest.fixture
def runner():
    return CliRunner()


@pytest.fixture
def db_path(tmp_path):
    return str(tmp_path / "test.db")


@pytest.fixture
def populated_db(tmp_path):
    """A database pre-populated with a project, sprint, tasks, and time entries."""
    db = Database(path=tmp_path / "populated.db")
    p = db.create_project("My App", "A great application")
    s = db.create_sprint(p.id, "Sprint 1", goal="Launch v1")
    t1 = db.create_task(p.id, "Write API", priority=Priority.HIGH, estimate_hours=3.0, sprint_id=s.id)
    t2 = db.create_task(p.id, "Build UI", priority=Priority.MEDIUM, estimate_hours=5.0, sprint_id=s.id)
    t3 = db.create_task(p.id, "Write tests", priority=Priority.HIGH, estimate_hours=2.0)
    db.update_task(t1.id, status=Status.DONE)
    db.update_task(t2.id, status=Status.IN_PROGRESS)
    db.log_time(t1.id, 2.5, "backend done")
    db.log_time(t1.id, 0.5, "fixes")
    db.log_time(t2.id, 3.0, "frontend work")
    return db, p, s, [t1, t2, t3]


def invoke(runner, db_path, *args):
    return runner.invoke(cli, ["--db", db_path, *args], catch_exceptions=False)


