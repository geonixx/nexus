# ── CLI integration tests ─────────────────────────────────────────────────────

def _setup_project(runner, db_path):
    invoke(runner, db_path, "project", "new", "Export Test", "-d", "Export me")
    invoke(runner, db_path, "sprint", "new", "1", "S1", "-g", "Goal")
    invoke(runner, db_path, "task", "add", "1", "Task Alpha", "-p", "high", "-e", "2.0", "-s", "1")
    invoke(runner, db_path, "task", "add", "1", "Task Beta", "-p", "medium")
    invoke(runner, db_path, "task", "done", "1")
    invoke(runner, db_path, "task", "log", "1", "1.5", "-n", "first pass")


# export markdown ──────────────────────────────────────────────────────────────

def test_export_markdown_stdout(runner, db_path):
    _setup_project(runner, db_path)
    result = invoke(runner, db_path, "export", "markdown", "1", "--stdout")
    assert result.exit_code == 0
    assert "# Export Test" in result.output
    assert "Task Alpha" in result.output
    assert "Task Beta" in result.output


def test_export_markdown_to_file(runner, db_path, tmp_path):
    _setup_project(runner, db_path)
    out = str(tmp_path / "project.md")
    result = invoke(runner, db_path, "export", "markdown", "1", "-o", out)
    assert result.exit_code == 0
    assert "Exported" in result.output
    content = Path(out).read_text()
    assert "Export Test" in content
    assert "first pass" in content


def test_export_markdown_default_filename(runner, db_path, tmp_path):
    """Without -o, should write to <slug>.md in the CWD."""
    _setup_project(runner, db_path)
    with runner.isolated_filesystem():
        result = runner.invoke(
            cli, ["--db", db_path, "export", "markdown", "1"], catch_exceptions=False
        )
    assert result.exit_code == 0
    assert "export-test.md" in result.output


def test_export_markdown_missing_project(runner, db_path):
    result = invoke(runner, db_path, "export", "markdown", "999")
    assert result.exit_code == 1


